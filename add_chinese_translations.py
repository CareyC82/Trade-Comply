def translate_text(text):
    """简单的文本翻译函数"""
    if not text:
        return text
    result = text
    for en_word, zh_word in common_translations.items():
        result = result.replace(en_word, zh_word)
    return result

def process_tags_file(input_path, output_path):
    """处理tags.json文件，添加中文翻译字段"""
    with open(input_path, 'r', encoding='utf-8') as f:
        tags = json.load(f)
    
    for tag in tags:
        # 添加中文分类
        tag['category_zh'] = category_translations.get(tag['category'], tag['category'])
        
        # 添加中文标签类型
        tag['tag_type_zh'] = tag_type_translations.get(tag['tag_type'], tag['tag_type'])
        
        # 添加中文描述
        tag['description_zh'] = translate_text(tag.get('description', ''))
        
        # 添加中文简短描述
        tag['short_description_zh'] = translate_text(tag.get('short_description', ''))
        
        # 添加中文来源引用
        tag['source_citation_zh'] = translate_text(tag.get('source_citation', ''))
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(tags, f, ensure_ascii=False, indent=2)
    
    print(f"Processed {len(tags)} tags")

def process_cases_file(input_path, output_path):
    """处理cases.json文件，添加中文翻译字段"""
    with open(input_path, 'r', encoding='utf-8') as f:
        cases = json.load(f)
    
    for case in cases:
        # 添加中文标题
        case['title_zh'] = translate_text(case.get('title', ''))
        
        # 添加中文摘要
        case['summary_zh'] = translate_text(case.get('summary', ''))
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(cases, f, ensure_ascii=False, indent=2)
    
    print(f"Processed {len(cases)} cases")

if __name__ == "__main__":
    process_tags_file('data/tags.json', 'data/tags.json')
    process_cases_file('data/cases.json', 'data/cases.json')
